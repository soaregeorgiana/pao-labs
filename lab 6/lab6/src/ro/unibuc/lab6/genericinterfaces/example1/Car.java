package ro.unibuc.lab6.genericinterfaces.example1;

//generics usages
public class Car {

    private String model;
    private String year;

    public Car() {
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }
}
